var node1 = document.getElementById('node1');

node1.addEventListener("mouseover", highlight);
node1.addEventListener("mouseout", delight);

function highlight(){
    node1.style = "cursor:pointer"
    var g1 = document.getElementById('Layer01');
    var c = g1.childNodes;
    num = c.length
    for(var i = 0; i< num -1; i++){
        if(i%2 !== 0){
            c[i].style = "stroke-width:3px;stroke: #fff"
        }
    }
    
}
function delight(){
    var g1 = document.getElementById('Layer01');
    var c = g1.childNodes;
    num = c.length
    for(var i = 0; i< num -1; i++){
        if(i%2 !== 0){
            c[i].style = "stroke-width:2px"
        }
    }
}
